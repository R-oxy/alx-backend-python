#!/usr/bin/env python3
"""
This module defines a function to_str that
returns the string representation of a float.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of the given float.

    Args:
        n (float): The float number.

    Returns:
        str: The string representation of the float.
    """
    return (str(n))
